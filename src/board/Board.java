package board;

public class Board {
    String BoardName;
    String BoardText;
    String BoardWriter;
    public Board (String BoardName,String BoardText,String BoardWriter){
        this.BoardName = BoardName;
        this.BoardText = BoardText;
        this.BoardWriter = BoardWriter;
    }

    public String getBoardName(){
        return getBoardName();
    }

    public String getBoardText(){
        return getBoardName();
    }
    public String getBoardWriter(){
        return getBoardName();
    }

}
